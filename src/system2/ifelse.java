public class ifelse {
	public static void main(String[] args) {
		int marks=89;
		if(marks>=70)
		{
			System.out.println("distingtion");
		}
		else if(marks>=60)
		{
			System.out.println("first class");
		}
		else if(marks>=35)
		{
			System.out.println("pass");
		}
		else
		{
			System.out.println("fail");
		}
	}

}
